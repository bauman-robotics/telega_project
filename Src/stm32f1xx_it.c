/**
  * @brief This function handles EXTI line0 interrupt.
  */
void EXTI0_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI0_IRQn 0 */
  if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0)){ //check pin state 
    in_signal = 1;
		speed_drv1 = 0.1;
		privod_position = 10;
		HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_SET);
  } else {
		in_signal = 0;
		HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_RESET);
		speed_drv1 = -0.1;
		privod_position = 180;
	}

	flag_in_signal_interrupt = 1; 
	
  /* USER CODE END EXTI0_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_0);
  /* USER CODE BEGIN EXTI0_IRQn 1 */

  /* USER CODE END EXTI0_IRQn 1 */
}

/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
	static uint8_t counter;
	
	if (USART1->SR&USART_SR_RXNE) // if data received
	{
		buf_uplevel[counter] = USART1->DR;
		if (buf_uplevel[counter] == 0)
		{
			if (counter == 10)
				uart_uplevel_ready = 1;		
			counter = 0;
			return ;
		}
		if (counter < 11)
			counter++; // BUF_SIZE_UPLEVEL
		else
			counter = 0;
		
	}

  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

